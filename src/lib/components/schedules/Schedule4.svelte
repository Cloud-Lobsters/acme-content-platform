	<h4>SECTION I</h4>

	<div class="clause-section">
		<h5>Clause 1</h5>
		<p class="clause-title"><em>Purpose and scope</em></p>

		<div class="clause-content">
			<div class="clause-item">
				<span class="label">(a)</span>
				<p>
					The purpose of these standard contractual clauses is to ensure compliance with the
					requirements of Regulation (EU) 2016/679 of the European Parliament and of the Council
					of 27 April 2016 on the protection of natural persons with regard to the processing of
					personal data and on the free movement of such data (General Data Protection Regulation)
					for the transfer of personal data to a third country.
				</p>
			</div>

			<div class="clause-item">
				<span class="label">(b)</span>
				<p>The Parties:</p>
				<div class="sub-item">
					<span class="sub-label">(i)</span>
					<p>
						the natural or legal person(s), public authorities, agencies or other bodies
						(hereinafter "entity/ies") transferring the personal data, as listed in Annex I.A
						(hereinafter each "data exporter"), and
					</p>
				</div>
				<div class="sub-item">
					<span class="sub-label">(ii)</span>
					<p>
						the entity/ies in a third country receiving the personal data from the data exporter,
						directly or indirectly via another entity also Party to these Clauses, as listed in
						Annex I.A. (hereinafter each "data importer")
					</p>
				</div>
				<p>
					have agreed to these standard contractual clauses (hereinafter: "Clauses").
				</p>
			</div>

			<div class="clause-item">
				<span class="label">(c)</span>
				<p>These Clauses apply with respect to the transfer of personal data as specified in Annex I.B.</p>
			</div>

			<div class="clause-item">
				<span class="label">(d)</span>
				<p>
					The Appendix to these Clauses containing the Annexes referred to therein forms an
					integral part of these Clauses.
				</p>
			</div>
		</div>
	</div>

	<div class="clause-section">
		<h5>Clause 2</h5>
		<p class="clause-title"><em>Effect and invariability of the Clauses</em></p>

		<div class="clause-content">
			<div class="clause-item">
				<span class="label">(a)</span>
				<p>
					These Clauses set out appropriate safeguards, including enforceable data subject rights
					and effective legal remedies, pursuant to Article 46(1) and Article 46(2)(c) <em
						>of Regulation (EU) 2016/679</em
					> and, with respect to data transfers from controllers to processors and/or processors to
					processors, standard contractual clauses pursuant to Article 28(7) <em
						>of Regulation (EU) 2016/679</em
					>, provided they are not modified, except to select the appropriate Module(s) or to add or
					update information in the Appendix. This does not prevent the Parties from including the
					standard contractual clauses laid down in these Clauses in a wider contract and/or to add
					other clauses or additional safeguards, provided that they do not contradict, directly or
					indirectly, these Clauses or prejudice the fundamental rights or freedoms of data subjects.
				</p>
			</div>

			<div class="clause-item">
				<span class="label">(b)</span>
				<p>
					These Clauses are without prejudice to obligations to which the data exporter is subject by
					virtue of Regulation (EU) 2016/679.
				</p>
			</div>
		</div>
	</div>

	<div class="clause-section">
		<h5>Clause 3</h5>
		<p class="clause-title"><em>Third-party beneficiaries</em></p>

		<div class="clause-content">
			<div class="clause-item">
				<span class="label">(a)</span>
				<p>
					Data subjects may invoke and enforce these Clauses, as third-party beneficiaries, against
					the data exporter and/or data importer, with the following exceptions:
				</p>
				<div class="sub-item">
					<span class="sub-label">(i)</span>
					<p>Clause 1, Clause 2, Clause 3, Clause 6, Clause 7;</p>
				</div>
				<div class="sub-item">
					<span class="sub-label">(ii)</span>
					<p>
						Clause 8 - Module One: Clause 8.5 (e) and Clause 8.9(b); Module Two: Clause 8.1(b),
						8.9(a), (c), (d) and (e); Module Three: Clause 8.1(a), (c) and (d) and Clause 8.9(a),
						(c), (d), (e), (f) and (g); Module Four: Clause 8.1 (b) and Clause 8.3(b);
					</p>
				</div>
				<div class="sub-item">
					<span class="sub-label">(iii)</span>
					<p>
						Clause 9 - Module Two: Clause 9(a), (c), (d) and (e); Module Three: Clause 9(a), (c),
						(d) and (e);
					</p>
				</div>
				<div class="sub-item">
					<span class="sub-label">(iv)</span>
					<p>
						Clause 12 - Module One, Two and Three: Clause 12(a) and (d); Modules Two and Three:
						Clause 12(g), (h) and (i);
					</p>
				</div>
				<div class="sub-item">
					<span class="sub-label">(v)</span>
					<p>Clause 13;</p>
				</div>
				<div class="sub-item">
					<span class="sub-label">(vi)</span>
					<p>Clause 15.1(c), (d) and (e);</p>
				</div>
				<div class="sub-item">
					<span class="sub-label">(vii)</span>
					<p>Clause 16(e);</p>
				</div>
				<div class="sub-item">
					<span class="sub-label">(viii)</span>
					<p>
						Clause 18 - Modules One, Two and Three: Clause 18(a) and (b); Module Four: Clause
						18.
					</p>
				</div>
			</div>

			<div class="clause-item">
				<span class="label">(b)</span>
				<p>
					Paragraph (a) is without prejudice to rights of data subjects under Regulation (EU)
					2016/679.
				</p>
			</div>
		</div>
	</div>

	<div class="clause-section">
		<h5>Clause 4</h5>
		<p class="clause-title"><em>Interpretation</em></p>

		<div class="clause-content">
			<div class="clause-item">
				<span class="label">(a)</span>
				<p>
					Where these Clauses use terms that are defined in Regulation (EU) 2016/679, those terms
					shall have the same meaning as in that Regulation.
				</p>
			</div>

			<div class="clause-item">
				<span class="label">(b)</span>
				<p>
					These Clauses shall be read and interpreted in the light of the provisions of Regulation
					(EU) 2016/679.
				</p>
			</div>

			<div class="clause-item">
				<span class="label">(c)</span>
				<p>
					These Clauses shall not be interpreted in a way that conflicts with rights and obligations
					provided for in Regulation (EU) 2016/679.
				</p>
			</div>
		</div>
	</div>

	<div class="clause-section">
		<h5>Clause 5</h5>
		<p class="clause-title"><em>Hierarchy</em></p>

		<div class="clause-content">
			<p>
				In the event of a contradiction between these Clauses and the provisions of related
				agreements between the Parties, existing at the time these Clauses are agreed or entered
				into thereafter, these Clauses shall prevail.
			</p>
		</div>
	</div>

