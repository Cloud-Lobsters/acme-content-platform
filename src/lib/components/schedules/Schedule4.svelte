	<h1>Schedule 4</h1>
	<h2>Standard Contractual Clauses and the UK Addendum</h2>

	<h3>STANDARD CONTRACTUAL CLAUSES</h3>

	<div class="section-box highlighted">
		<p class="note-text">(b)</p>
