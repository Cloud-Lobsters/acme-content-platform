	<div class="clause-section">
		<h5>Clause 6</h5>
		<p class="clause-title"><em>Description of the transfer(s)</em></p>

		<div class="clause-content">
			<p>
				The details of the transfer(s), and in particular the categories of personal data that are
				transferred and the purpose(s) for which they are transferred, are specified in Annex I.B.
			</p>
		</div>
	</div>

	<div class="clause-section">
		<p class="clause-title"><em>Clause 7 - Optional</em></p>
		<p class="clause-title"><em>Docking clause</em></p>

		<div class="clause-content">
			<div class="clause-item">
				<span class="label">(a)</span>
				<p>
					An entity that is not a Party to these Clauses may, with the agreement of the Parties,
					accede to these Clauses at any time, either as a data exporter or as data importer, by
					completing the Appendix and signing Annex I.A.
				</p>
			</div>

			<div class="clause-item">
				<span class="label">(b)</span>
				<p>
					Once it has completed the Appendix and signed Annex I.A, the acceding entity shall become a
					Party to these Clauses and have the rights and obligations of a data exporter or data
					importer in accordance with its designation in Annex I.A.
				</p>
			</div>

			<div class="clause-item">
				<span class="label">(c)</span>
				<p>
					The acceding entity shall have no rights or obligations arising under these Clauses from
					the period prior to becoming a Party.
				</p>
			</div>
		</div>
