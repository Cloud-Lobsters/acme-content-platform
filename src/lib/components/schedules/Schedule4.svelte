	<h4>SECTION II – OBLIGATIONS OF THE PARTIES</h4>

	<div class="clause-section">
		<h5>Clause 8</h5>
		<p class="clause-title"><em>Data protection safeguards</em></p>

		<div class="clause-content">
			<p>
				The data exporter warrants that it has used reasonable efforts to determine that the data
				importer is able, through the implementation of appropriate technical and organisational
				measures, to satisfy its obligations under these Clauses.
			</p>
		</div>
	</div>

	<div class="module-section">
		<h4>MODULE ONE: Transfer controller to controller</h4>

		<div class="clause-content">
			<h5>8.1 Purpose limitation</h5>
			<p>
				The data importer shall process the personal data only for the specific purpose(s) of the
				transfer, as set out in Annex I.B. It may only process the personal data for another
				purpose:
			</p>

			<div class="clause-item">
				<span class="label">(i)</span>
				<p>where it has obtained the data subject's prior consent;</p>
			</div>

			<div class="clause-item">
				<span class="label">(ii)</span>
				<p>
					where necessary for the establishment, exercise or defence of legal claims in the context
					of specific administrative, regulatory or judicial proceedings; or
				</p>
			</div>

			<div class="clause-item">
				<span class="label">(iii)</span>
				<p>
					where necessary in order to protect the vital interests of the data subject or of another
					natural person.
				</p>
			</div>
		</div>

		<div class="clause-content">
			<h5>8.2 Transparency</h5>

			<div class="clause-item">
				<span class="label">(a)</span>
				<p>
					In order to enable data subjects to effectively exercise their rights pursuant to Clause
					11, the data importer shall inform them, either directly or through the data exporter:
				</p>
				<div class="sub-item">
					<span class="sub-label">(i)</span>
					<p>of its identity and contact details;</p>
				</div>
				<div class="sub-item">
					<span class="sub-label">(ii)</span>
					<p>of the categories of personal data processed;</p>
				</div>
			</div>
		</div>

		<div class="clause-content">
			<h5>8.3 Accuracy and data minimisation</h5>

			<div class="clause-item">
				<span class="label">(a)</span>
				<p>
					Each Party shall ensure that the personal data is accurate and, where necessary, kept up
					to date. The data importer shall take every reasonable step to ensure that personal data
					that is inaccurate, having regard to the purpose(s) of processing, is erased or rectified
					without delay.
				</p>
			</div>

			<div class="clause-item">
				<span class="label">(b)</span>
				<p>
					If one of the Parties becomes aware that the personal data it has transferred or received
					is inaccurate, or has become outdated, it shall inform the other Party without undue delay.
				</p>
			</div>

			<div class="clause-item">
				<span class="label">(c)</span>
				<p>
					The data importer shall ensure that the personal data is adequate, relevant and not
					excessive in relation to the purpose(s) of processing.
				</p>
			</div>
		</div>

		<div class="clause-content">
			<h5>8.4 Storage limitation</h5>
			<p>
				The data importer shall retain the personal data for no longer than necessary for the
				purposes for which it is processed. It shall put in place appropriate technical or
				organisational measures to ensure compliance with this obligation, including erasure or
				anonymisation of the data and all back-ups at the end of the retention period.
			</p>
		</div>

		<div class="clause-content">
			<h5>8.5 Security of processing</h5>

			<div class="clause-item">
				<span class="label">(a)</span>
				<p>
					The data importer and, during transmission, also the data exporter shall implement
					appropriate technical and organisational measures to ensure the security of the personal
					data, including protection against a breach of security leading to accidental or unlawful
					destruction, loss, alteration, unauthorised disclosure or access (hereinafter "personal
					data breach"). In assessing the appropriate level of security, they shall take due account
					of the state of the art, the costs of implementation, the nature, scope, context and
					purpose(s) of processing and the risks involved in the processing for the data subject.
					The Parties shall in particular consider having recourse to encryption or pseudonymisation,
					including during transmission, where the purpose of processing can be fulfilled in that
					manner.
				</p>
			</div>

			<div class="note-box">
				<p>
					This requires retaining the data anonymous in such a way that the individual is no longer
					identifiable by anyone, in line with the definition of personal data in Article 4(1) of
					Regulation (EU) 2016/679, and that this process is irreversible.
				</p>
			</div>
		</div>
