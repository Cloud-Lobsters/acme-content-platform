<p><strong>13.1</strong> For the purposes of this Clause 13:</p>
	<div class="sub-clause">
		<p>
			<strong>"Associated Person"</strong> shall have the meaning given to it in the Bribery Act
			2010 (the Act).
		</p>
		<p>
			<strong>"Adequate Procedures"</strong> shall mean policies and procedures which a party reasonably
			believes would provide it with a defence to an offence under section 7(1) of the Act as such
			defence is set out in section 7(2) of the Act (with reference to any guidance issued under section
			9 of the Act).
		</p>
	</div>

	<p><strong>13.2</strong> Each party shall:</p>
	<div class="sub-clause">
		<p>
			<strong>13.2.1</strong> comply with all Applicable Laws, statutes, regulations, and codes relating
			to anti-bribery (including but not limited to the Act) in all jurisdictions applicable to this
			Agreement ("Relevant Requirements");
		</p>
		<p>
			<strong>13.2.2</strong> not engage in any activity, practice or conduct which would constitute
			an offence under sections 1, 2 or 6 of the Act whether or not such activity, practice or conduct
			had been carried out in the United Kingdom;
		</p>
		<p>
			<strong>13.2.3</strong> have and shall maintain in place at all times Adequate Procedures and
			shall ensure compliance by its Associated Persons with the Relevant Requirements and Clause
			13.2.1, and shall enforce their provisions where appropriate;
		</p>
		<p>
			<strong>13.2.4</strong> promptly report to the other party any request or demand for any undue
			financial or other advantage of any kind received by it in connection with the performance of
			its obligations under the Agreement (the Sub-Contractor shall use the process set out in Clause
			13.2.5 below); and
		</p>
		<p>
			<strong>13.2.5</strong> from time to time and at the reasonable request the other party, confirm
			in writing to the other party that it has complied with its undertakings under Clause 13 and
			shall provide any information reasonably requested by the other party to evidence such compliance,
			including reasonable details of its Adequate Procedures.
		</p>
	</div>

	<p>
		<strong>13.3</strong> In accordance with Clause 13.2.4, where the Sub-Contractor becomes aware
		at any time of any requests from Associated Persons of the Sub-Contractor or the Main Contractor,
		which it considers could be in breach of the Relevant Requirements it will immediately report
		such concerns to Main Contractor's UK Ethics and Compliance Officer at the email address: ethics.uk@capgemini.com
	</p>

	<p>
		<strong>13.4</strong> Each party acknowledges that any breach by it of this Clause 13 shall constitute
		a material breach of the Agreement and any Statement of Work.
	</p>
