	<p>
		<strong>17.4</strong> If the dispute is not resolved by escalation in accordance with Clause
		17.1, the parties will seek to resolve disputes between them by an Alternative Dispute Resolution
		technique recommended by the Centre for Effective Dispute Resolution. Neither party may initiate
		any legal action until the escalation in Clause 17 has been exhausted and either party has refused
		to submit to Alternative Dispute Resolution.
	</p>

