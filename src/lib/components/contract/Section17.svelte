<p>
		<strong>17.1</strong> Any question or difference which may arise concerning the construction
		meaning or effect of this Agreement and/or any Statement of Work or any matter arising out of
		or in connection with this Agreement and/or any Statement of Work shall in the first instance
		be referred to the Project Manager and the Representative for discussion and resolution at or
		by the next regular management meeting. If the matter is not resolved at this meeting the matter
		will be referred to the next level of the Sub-Contractor and the Main Contractor's management
		who must meet within two (2) weeks to attempt to resolve the matter. If the matter is not resolved
		at that meeting the escalation continues with the same maximum time intervals up to two (2) more
		levels of management. If the unresolved matter is having a serious effect on the Sub-Contract
		Work the parties will use reasonable endeavours to reduce the elapsed time in completing the
		process. The Sub-Contractor may not initiate any legal action or purport to terminate this Agreement
		and/or a Statement of Work an until the process has been completed unless such party has reasonable
		cause to do so to avoid damage to its business or to protect or preserve any right of action
		it may have.
	</p>

