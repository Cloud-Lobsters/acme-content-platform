<p>
		<strong>11.1</strong> This Agreement shall come into full force and effect on the Commencement
		Date.
	</p>

	<p>
		<strong>11.2</strong> Termination of a Statement of Work shall not affect the continuance of
		this Agreement or any other Statement of Work then in force. Neither shall termination or expiry
		of this Agreement affect the continuance of any Statement of Work then in force.
	</p>

	<p>
		<strong>11.3</strong> Either party may terminate this Agreement or a Statement of Work at any
		time by giving notice in writing to the other party if:
	</p>
	<div class="sub-clause">
		<p>
			<strong>11.3.1</strong> the other party commits a material breach of this Agreement and such
			breach is not remediable;
		</p>
		<p>
			<strong>11.3.2</strong> the other party commits a material breach of this Agreement which
			is not remedied within 14 days of receiving written notice of such breach;
		</p>
		<p>
			<strong>11.3.3</strong> any consent, licence or authorisation held by the other party is revoked
			or modified such that the other party is no longer able to comply with its obligations under
			this Agreement or receive any benefit to which it is entitled.
		</p>
	</div>

	<p>
		<strong>11.4</strong> Either party may terminate this Agreement at any time by giving notice
		in writing to the other party if that other party:
	</p>
	<div class="sub-clause">
		<p>
			<strong>11.4.1</strong> stops carrying on all or a significant part of its business, or indicates
			in any way that it intends to do so;
		</p>
		<p>
			<strong>11.4.2</strong> is unable to pay its debts either within the meaning of section 123
			of the Insolvency Act 1986 or if the non-defaulting party reasonably believes that to be the
			case;
		</p>
		<p>
			<strong>11.4.3</strong> becomes the subject of a company voluntary arrangement under the Insolvency
			Act 1986;
		</p>
		<p>
			<strong>11.4.4</strong> has a receiver, manager, administrator or administrative receiver appointed
			over all or any part of its undertaking, assets or income;
		</p>
		<p><strong>11.4.5</strong> has a resolution passed for its winding up;</p>
		<p>
			<strong>11.4.6</strong> has a petition presented to any court for its winding up or an application
			is made for an administration order, or any winding-up or administration order is made against
			it;
		</p>
		<p>
			<strong>11.4.7</strong> is subject to any procedure for the taking control of its goods that
			is not withdrawn or discharged within seven days of that procedure being commenced;
		</p>
		<p><strong>11.4.8</strong> has a freezing order made against it;</p>
		<p>
			<strong>11.4.9</strong> is subject to any recovery or attempted recovery of items supplied
			to it by a supplier retaining title to those items; and
		</p>
		<p>
			<strong>11.4.10</strong> is subject to any events or circumstances analogous to those in clauses
			11.4.1 to 11.4.9 in any jurisdiction.
		</p>
	</div>

	<p>
		<strong>11.5</strong> Either party may terminate this Agreement at any time by giving not less
		than four weeks' notice in writing to the other party if the other party undergoes a change
		of control or if it is realistically anticipated that it will undergo a change of control within
		two months. For the purposes of this clause 11.5, 'control' shall mean beneficial ownership
		of more than 50% of the issued share capital of a company or the legal power to direct or cause
		the direction of the management of a company.
	</p>

	<p>
		<strong>11.6</strong> The right of a party to terminate the Agreement pursuant to clause 11.5
		shall not apply to the extent that the relevant procedure is entered into for the purpose of
		amalgamation, reconstruction or merger (where applicable) where the amalgamated, reconstructed
		or merged party agrees to adhere to this Agreement.
	</p>

	<p>
		<strong>11.7</strong> If a party becomes aware that any event has occurred, or circumstances
		exist, which may entitle the other party to terminate this Agreement under this clause 11, it
		shall immediately notify the other party in writing.
	</p>

	<p>
		<strong>11.8</strong> Termination or expiry of this Agreement will not affect any accrued rights
		and liabilities of either party at any time up to the date of termination.
	</p>

	<p>
		<strong>11.9</strong> Where the Statements of Work under this Agreement relate to only one Customer,
		this Agreement shall terminate immediately if (i) the Main Contract is terminated by the Customer
		for any reason (ii) any payment due from the Customer to the Main Contractor is not made under
		the Main Contract or (iii) the Customer withdraws its consent to sub-contract the Sub-Contract
		Work to the Sub-Contractor. Where the Statements of Work under this Agreement relate to more
		than one Customer, the relevant Statement of Work(s) shall terminate immediately if: (i) the
		relevant Main Contract is terminated by the Customer for any reason, or (ii) any payment due
		from the Customer to the Main Contractor is not made under the relevant Main Contract, or (iii)
		if the Customer withdraws its consent to sub-contract the relevant Sub-Contract Work to the Sub-Contractor.
	</p>

	<p>
		<strong>11.10</strong> On termination of this Agreement or a Statement of Work for any reason,
		Sub-Contractor will provide Main Contractor with any assistance necessary to ensure that an orderly
		transfer is achieved with minimal disruption, to Main Contractor or any person nominated by Main
		Contractor, of the Deliverables and Sub-Contract Work, functions and operations which were provided
		prior to termination by the other party. The provisions of this Clause will apply whatever the
		reason for termination.
	</p>
