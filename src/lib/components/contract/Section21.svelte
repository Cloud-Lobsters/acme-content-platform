<p>
		<strong>21.1</strong> Any notice or other communication given by a party under this Agreement
		or a Statement of Work shall:
	</p>
	<div class="sub-clause">
		<p><strong>21.1.1</strong> be in writing and in English;</p>
		<p><strong>21.1.2</strong> be signed by, or on behalf of, the party giving it; and</p>
		<p>
			<strong>21.1.3</strong> be sent to the relevant party at the address set out in clause 21.3.
		</p>
	</div>

	<p><strong>21.2</strong> Notices may be given, and are deemed received:</p>
	<div class="sub-clause">
		<p><strong>21.2.1</strong> by hand: on receipt of a signature at the time of delivery;</p>
		<p>
			<strong>21.2.2</strong> by Royal Mail Recorded Signed For post: on the second Business Day
			after posting;
		</p>
		<p>
			<strong>21.2.3</strong> by post: forty-eight (48) hours after a first class registered letter
			is posted to the appropriate address.
		</p>
	</div>

	<p>
		<strong>21.3</strong> Notices and other communications shall be sent to the registered office
		stated in the Agreement (or any other address notified for this purpose by that party).
	</p>

	<p>
		<strong>21.4</strong> All references to time are to the local time at the place of deemed receipt.
	</p>

	<p>
		<strong>21.5</strong> A notice given under this Agreement is not validly served if sent by email.
	</p>
