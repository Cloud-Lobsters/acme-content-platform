	<p>
		<strong>3.2</strong> The Sub-Contractor's employees, subcontractors and agents shall have
		reasonable access to and use of existing facilities within any premises where relevant to its
		responsibilities under a Statement of Work and, such access is granted subject to Clause 3.3
		and any, rights, obligations or restrictions specified in the Main Contract; and
	</p>

	<p>
		<strong>3.3</strong> The Sub-Contractor will ensure that its employees and subcontractors comply
		with such rules, regulations and practices whilst on premises which are under Customer and/or
		Main Contractor's control. In any case, the Sub-Contractor shall at all times be responsible
		for the actions of its personnel, the personnel of its sub-contractors and visitors while these
		personnel and visitors are at the premises and the said responsibility shall extend to all acts
		of such personnel while they are on any of the said premises whether or not such acts fall
		within the scope and course of their employment.
	</p>

	<p>
		<strong>3.4</strong> The Sub-Contractor shall achieve the Sub-Contract Work and each part of them
		by the corresponding date[s] set out in the relevant Statement of Work. If the Sub-Contractor
		fails to comply with the provisions of this Clause 3.4, due to its Default, then the Main
		Contractor may:
	</p>
	<div class="sub-clause">
		<p>
			<strong>3.4.1</strong> refuse to accept any subsequent attempts to perform the Sub-Contract
			Works (or part thereof) and terminate the Statement of Work immediately by written notice to
			the Sub-Contractor;
		</p>
		<p>
			<strong>3.4.2</strong> procure work similar to the Sub-Contract Work from an alternative
			supplier; and
		</p>
		<p>
			<strong>3.4.3</strong> recover from the Sub-Contractor all Losses, damages, costs and expenses
			incurred by the Main Contractor arising from the Sub-Contractor's default.
		</p>
	</div>

	<p>
		<strong>3.5</strong> The Sub-Contractor shall not (during the term of the applicable Statement of
		Work and for 6 (six) months afterwards), without the Main Contractor's written consent, offer
		employment to any of the Main Contractor's personnel who have been engaged in any Sub-Contract
		in relation to a Statement of Work. However, this restriction shall not apply to any person who
		(without having been approached directly or indirectly) responds to a general recruitment advert
		placed by or on behalf of the new employer. If the Sub-Contractor breaches this Clause 3.5, the
		Sub-Contractor shall promptly pay to the Main Contractor a sum equal to the annual salary of the
		employee in question (net of benefits) and it is agreed that this amount is a genuine
		pre-estimate of the loss that the Main Contractor is likely to suffer as a result of such
		breach.
	</p>

	<p>
		<strong>3.6</strong> Notwithstanding, clause 3.5, the Sub-Contractor agrees that any restrictions
		in the Main Contract relating to the non-solicitation of personnel shall be construed as placing
		restrictions on the Sub-Contractor with regards to both the solicitation of the Customer's and
		the Main Contractor's personnel.
	</p>
