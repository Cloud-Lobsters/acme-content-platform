			providing its services to the Customer);
		</p>
		<p id="3.1.3">
			<strong>3.1.3</strong> achieve the Sub-Contract Work and each part of them by the
			corresponding date(s) set out in the Statement of Work and in any event to enable the Main
			Contractor to have reasonable time to fulfil its obligations to the Customer;
		</p>
		<p>
			<strong>3.1.4</strong> co-operate with the Main Contractor and the Customer where required by
			the Main Contractor in all matters arising under this Agreement and any Statement of Work;
		</p>
		<p>
			<strong>3.1.5</strong> provide all information, documents, materials, data or other items
